<template>
  <div class="vue-root">
    <div class="demo-description">
      <h1>Grid Row Reorder</h1>
      <p>
        The example demonstrates the row reorder feature of the grid component.
        Press the left mouse button or Tap, while you are on a row header and move
        it around to start drag. A feedback and reorder line will be shown, while
        you are in the Grid's bounds. Drop it to the position you wish to reorder
        the rows.
      </p>
    </div>
    <smart-grid id="grid"></smart-grid>
  </div>
</template>

<script>
import { onMounted } from "vue";
import "smart-webcomponents/source/styles/smart.default.css";
import "smart-webcomponents/source/modules/smart.grid.js";

export default {
  name: "app",
  setup() {
    onMounted(() => {
      window.Smart(
        "#grid",
        class {
          get properties() {
            return {
              dataSource: new window.Smart.DataAdapter({
                dataSource: window.generateData(20),
                dataFields: [
                  "id: number",
                  "firstName: string",
                  "lastName: string",
                  "productName: string",
                  "quantity: number",
                  "price: number",
                  "total: number"
                ]
              }),
              sorting: {
                enabled: true
              },
              behavior: {
                allowRowReorder: true
              },
              appearance: {
                showRowHeader: true,
                showRowHeaderDragIcon: true
              },
              columns: [
                {
                  label: "#",
                  width: 200,
                  dataField: "id"
                },
                {
                  label: "First Name",
                  dataField: "firstName"
                },
                {
                  label: "Last Name",
                  dataField: "lastName"
                },
                {
                  label: "Product",
                  dataField: "productName"
                },
                {
                  label: "Quantity",
                  dataField: "quantity",
                  align: "right",
                  cellsAlign: "right"
                },
                {
                  label: "Unit Price",
                  dataField: "price",
                  align: "right",
                  cellsAlign: "right",
                  cellsFormat: "c2"
                },
                {
                  label: "Total",
                  dataField: "total",
                  align: "right",
                  cellsAlign: "right",
                  cellsFormat: "c2"
                }
              ]
            };
          }
        }
      );
    });
  }
};
</script>

