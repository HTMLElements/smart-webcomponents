  name: "app",
  setup() {
    onMounted(() => {
      window.Smart(
        "#editor",
        class {
          get properties() {
            return {
              //Properties
              autoSave: true,
              autoLoad: true,
              autoSaveInterval: 500, //The interval in miliseconds
            };
          }
        }
      );

      const editor = document.querySelector("smart-editor");

      document
        .getElementById("saveState")
        .addEventListener("click", () => editor.saveState());
      document
        .getElementById("loadState")
        .addEventListener("click", () => editor.loadState());
      document
        .getElementById("deleteState")
        .addEventListener("click", () => editor.clearState());
      document
        .getElementById("autoSaveState")
        .addEventListener(
          "click",
          () => (editor.autoSaveState = !editor.autoSaveState)
        );
      document
        .getElementById("autoLoadState")
        .addEventListener(
          "click",
          () => (editor.autoLoadState = !editor.autoLoadState)
        );
    });
  },
};
